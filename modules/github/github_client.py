# Internal callback registry
_event_hooks = {
    "push": [],
    "pull_request": [],
    "issues": []
}

def register_hook(event_type: str, callback):
    """
    Register a callback for a GitHub event.
    Example event types: "push", "pull_request", "issues"
    """
    if event_type not in _event_hooks:
        raise ValueError(f"âŒ Unsupported event type: {event_type}")
    _event_hooks[event_type].append(callback)
    logger.debug(f"âœ… Registered hook for {event_type}: {callback.__name__}")

def dispatch_event(event_type: str, payload: dict):
    """
    Dispatch a GitHub webhook event to registered handlers.
    """
    hooks = _event_hooks.get(event_type, [])
    if not hooks:
        logger.info(f"ğŸ“­ No handlers registered for: {event_type}")
        return

    logger.info(f"ğŸ“¨ Dispatching '{event_type}' to {len(hooks)} hook(s)")
    for hook in hooks:
        try:
            hook(payload)
        except Exception as e:
            logger.exception(f"âŒ Error in {hook.__name__}: {e}")
