def get_repo_markdown_files(repo_full_name: str, branch: str = "main", etag: str = None):
    """
    Return a list of markdown files from the specified GitHub repo and branch.
    Uses conditional GET with ETag when available.
    """
    token = os.getenv("GH_TOKEN")
    headers = {
        "Accept": "application/vnd.github+json",
        "Authorization": f"Bearer {token}",
        "User-Agent": "Github-Agent"
    }
    if etag:
        headers["If-None-Match"] = etag

    url = f"https://api.github.com/repos/{repo_full_name}/contents/?ref={branch}"
    logger.info(f"ğŸŒ Fetching contents from GitHub: {url}")

    try:
        resp = requests.get(url, headers=headers)
    except Exception as e:
        logger.error(f"âŒ HTTP error while fetching repo contents: {e}")
        return []

    if resp.status_code == 304:
        logger.info("â© No changes detected (ETag matched).")
        return []

    if not resp.ok:
        logger.error(f"âŒ GitHub API error: {resp.status_code} - {resp.text}")
        return []

    new_etag = resp.headers.get("ETag")
    data = resp.json()

    md_files = [item for item in data if item["name"].lower().endswith(".md")]

    logger.info(f"ğŸ“„ Found {len(md_files)} Markdown files. New ETag: {new_etag}")
    return md_files

